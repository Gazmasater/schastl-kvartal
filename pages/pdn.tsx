const PolicyPage: FC = () => {
  const policyRef = useRef<HTMLDivElement>(null)

  const downloadPolicy = () => {
    const content = [
      'Политика в отношении обработки персональных данных',
      'на сайте https://kvartal-lipetsk.ru/',
      'Редакция от 15 июля 2026 г.',
      '',
      ...policySections.flatMap(({ title, paragraphs }) => [title, ...paragraphs, '']),
    ].join('\n')
    const file = new Blob([content], { type: 'text/plain;charset=utf-8' })
    const url = URL.createObjectURL(file)
    const link = document.createElement('a')

    link.href = url
    link.download = 'politika-obrabotki-personalnyh-dannyh.txt'
    document.body.appendChild(link)
    link.click()
    link.remove()
    URL.revokeObjectURL(url)
  }

  useEffect(() => {
    const isPolicyTarget = (target: EventTarget | null) => target instanceof Node && policyRef.current?.contains(target)

    const preventSelection = (event: Event) => {
      if (isPolicyTarget(event.target)) event.preventDefault()
    }

    const preventCopy = (event: Event) => event.preventDefault()

    const preventCopyShortcut = (event: KeyboardEvent) => {
      if ((event.ctrlKey || event.metaKey) && ['c', 'x'].includes(event.key.toLowerCase())) event.preventDefault()
    }

    document.addEventListener('selectstart', preventSelection, true)
    document.addEventListener('dragstart', preventSelection, true)
    document.addEventListener('contextmenu', preventSelection, true)
    document.addEventListener('copy', preventCopy, true)
    document.addEventListener('cut', preventCopy, true)
    document.addEventListener('keydown', preventCopyShortcut, true)

    return () => {
      document.removeEventListener('selectstart', preventSelection, true)
      document.removeEventListener('dragstart', preventSelection, true)
      document.removeEventListener('contextmenu', preventSelection, true)
      document.removeEventListener('copy', preventCopy, true)
      document.removeEventListener('cut', preventCopy, true)
      document.removeEventListener('keydown', preventCopyShortcut, true)
    }
  }, [])

  return (
    <>
      <Meta
        title="Политика обработки персональных данных — «Счастливый Квартал»"
        description="Политика в отношении обработки персональных данных пользователей сайта «Счастливый Квартал»."
        robots="noindex, follow"
      />
      <HomeTemplate navData={NavMock} footerData={FooterMock}>
        <Policy ref={policyRef}>
          <div className="MuiContainer-root MuiContainer-maxWidthXl">
            <article>
              <h1>Политика в отношении обработки персональных данных</h1>
              <p className="site">на сайте https://kvartal-lipetsk.ru/</p>
              <p className="edition">Редакция от 15 июля 2026 г.</p>
              <button type="button" onClick={downloadPolicy}>
                Скачать текст политики (.txt)
              </button>

              {policySections.map(({ title, paragraphs }) => (
                <section key={title}>
                  <h2>{title}</h2>
                  {paragraphs.map(paragraph => (
                    <p key={paragraph}>{paragraph}</p>
                  ))}
                </section>
              ))}
            </article>
          </div>
        </Policy>
      </HomeTemplate>
    </>
  )
}

export default PolicyPage
